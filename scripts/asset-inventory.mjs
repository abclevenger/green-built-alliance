/**
 * Scans mirrored HTML for unique asset URL patterns (wp-content, wp-includes, fonts, etc.)
 */
import { readdir, readFile, writeFile } from "fs/promises";
import { join, extname } from "path";
import { fileURLToPath } from "url";
import { dirname } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const MIRROR = join(ROOT, "public", "mirror");
const OUT = join(ROOT, "ASSET_INVENTORY.md");

const RE = /https?:\/\/(?:www\.)?greenbuilt\.org(\/[^"'>\s]+)/g;

function cleanPath(path) {
  let p = path.split("?")[0].split("#")[0];
  p = p.replace(/\)+$/, "");
  return p;
}

async function walk(dir, acc = []) {
  const entries = await readdir(dir, { withFileTypes: true });
  for (const e of entries) {
    const p = join(dir, e.name);
    if (e.isDirectory()) await walk(p, acc);
    else if (e.name === "index.html") acc.push(p);
  }
  return acc;
}

async function main() {
  const files = await walk(MIRROR);
  const paths = new Map();
  const exts = new Map();
  for (const f of files) {
    const html = await readFile(f, "utf8");
    let m;
    RE.lastIndex = 0;
    while ((m = RE.exec(html)) !== null) {
      const path = cleanPath(m[1]);
      if (!path.startsWith("/") || path.includes("&")) continue;
      paths.set(path, (paths.get(path) ?? 0) + 1);
      const ext = extname(path) || "(none)";
      exts.set(ext, (exts.get(ext) ?? 0) + 1);
    }
  }
  const sorted = [...paths.entries()].sort((a, b) => b[1] - a[1]);
  let md = `# Asset inventory (from mirrored HTML)\n\n`;
  md += `Generated by scanning \`public/mirror/**/index.html\` for \`https://www.greenbuilt.org/...\` references.\n\n`;
  md += `- **Mirrored HTML files scanned:** ${files.length}\n`;
  md += `- **Unique paths referenced:** ${paths.size}\n\n`;
  md += `## Extension counts (all URL references)\n\n`;
  md += `| Extension | Approx. refs |\n`;
  md += `|-----------|---------------|\n`;
  for (const [ext, c] of [...exts.entries()].sort((a, b) => b[1] - a[1])) {
    md += `| ${ext} | ${c} |\n`;
  }
  md += `\n## Top 200 unique paths by frequency\n\n`;
  md += `| Path | Ref count |\n`;
  md += `|------|----------|\n`;
  for (const [path, c] of sorted.slice(0, 200)) {
    md += `| \`${path}\` | ${c} |\n`;
  }
  md += `\n> Full fidelity loads CSS/JS/images from these URLs on the live host unless you run an asset download pass into \`public/\`.\n`;
  await writeFile(OUT, md, "utf8");
  console.log("Wrote", OUT, "unique paths:", paths.size);
}

main().catch(console.error);
